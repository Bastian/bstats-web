				abuse and measure plugin usage is based on our legitimate interest in maintaining a stable,
				secure service.
			</li>
			<li>
				<strong>Consent</strong>: Because bStats does not rely on personal-data-driven features, we
				generally do not request consent for collecting technical analytics. However, server owners
				may opt out (see Section 5).
			</li>
		</ul>

		<h3>5. How to Opt Out</h3>
		<ul>
			<li>
				<strong>Configuration File</strong>: Each plugin that includes bStats typically generates a
				<code>bStats</code> folder with a global config file. You can disable bStats here,
				preventing any further data transmission from any plugin.
			</li>
		</ul>

		<h3>6. Data Retention</h3>
		<ul>
			<li>
				<strong>Server Metrics</strong>: Aggregated metrics (non-personal) remain in our databases
				indefinitely, helping developers track historical trends.
			</li>
			<li>
				<strong>IP Addresses</strong>: Stored only up to
				<strong>60 minutes</strong> in a rate-limiting mechanism, then automatically erased.
			</li>
			<li>
				<strong>Backups</strong>: If backups are kept, IP addresses are typically not stored
				long-term. Any short-term logs are overwritten periodically.
			</li>
		</ul>

